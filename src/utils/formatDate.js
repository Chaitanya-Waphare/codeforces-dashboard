const formatDate = (timestamp) => {
    if (!timestamp) return 'Invalid date';
  
    const date = new Date(timestamp * 1000);
    return date.toLocaleString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit',
    });
  };
  
  export default formatDate;
  